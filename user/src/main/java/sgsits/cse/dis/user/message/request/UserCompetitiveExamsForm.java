package sgsits.cse.dis.user.message.request;

public class UserCompetitiveExamsForm {
	
	private Long userId;
	private String nameOfExam;
	private Double Score;
	private Long rank;
	private String registerationNo;
	private int year;
	
	public Long getUserId() {
		return userId;
	}
	public String getNameOfExam() {
		return nameOfExam;
	}
	public Double getScore() {
		return Score;
	}
	public Long getRank() {
		return rank;
	}
	public String getRegisterationNo() {
		return registerationNo;
	}
	public int getYear() {
		return year;
	}
	
	

}
