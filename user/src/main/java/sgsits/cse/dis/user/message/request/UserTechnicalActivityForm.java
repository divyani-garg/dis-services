package sgsits.cse.dis.user.message.request;

public class UserTechnicalActivityForm {
	
	private Long userId;
	private String type;
	private String subject;
	private String from;
	private String to;
	private String nameOfCoordinator;
	private String attendedOrganized;
	private String place;
	
	public Long getUserId() {
		return userId;
	}
	public String getType() {
		return type;
	}
	public String getSubject() {
		return subject;
	}
	public String getFrom() {
		return from;
	}
	public String getTo() {
		return to;
	}
	public String getNameOfCoordinator() {
		return nameOfCoordinator;
	}
	public String getAttendedOrganized() {
		return attendedOrganized;
	}
	public String getPlace() {
		return place;
	}
	
	

}
